    private void signupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupBtnActionPerformed
        // TODO add your handling code here:
        try {
            if(nameTxt.getText().trim().isEmpty()
                ||emailTxt.getText().trim().isEmpty()
                ||passwordTxt.getText().trim().isEmpty() ){
                JOptionPane.showMessageDialog(this, "fill all filled");
             
        }else{
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",6003);
            SignUpService service =  (SignUpService) theRegistry.lookup("signup");
            SignUp theSignUp = new SignUp();
           
            theSignUp.setFullname(nameTxt.getText());
            theSignUp.setEmail(emailTxt.getText());
            theSignUp.setPassword(passwordTxt.getText());
            theSignUp.setRole("User");

            SignUp theObj = service.addSignUp(theSignUp);
            if(theObj!=null){
                JOptionPane.showMessageDialog(this, "Data saved");
            }else{
                JOptionPane.showMessageDialog(this, "Data not saved");
            }}
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_signupBtnActionPerformed

